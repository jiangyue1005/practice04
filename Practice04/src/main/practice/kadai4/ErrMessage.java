package kadai4;

/**
 * エラーメッセージクラス
 * 
 * @author y_jiang
 */
public class ErrMessage {
	/** エラーメッセージ */
	protected String errMessage;

	/**
	 * エラーメッセージを取得する
	 * 
	 * @return エラーメッセージ
	 */
	public String getErrMessage() {
		return errMessage;
	}

	/**
	 * エラーメッセージを設定する
	 * 
	 * @param errMessage エラーメッセージ
	 */
	public void setErrMessage(String errMessage) {
		this.errMessage = errMessage;
	}

}
